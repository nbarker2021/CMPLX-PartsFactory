def check_envelope(prev_comps: Optional[List[float]], comps: List[float], mean_mass: float,
                   env: RelativityEnvelope) -> Tuple[bool, float, str]:
    if not env.enabled:
        return True, 0.0, "disabled"
    if prev_comps is None:
        ok = (mean_mass < 1.0) if env.require_mass_lt_1 else True
        return ok, 0.0, "init"
    delta = max(abs(a - b) for a, b in zip(prev_comps, comps))
    if env.require_mass_lt_1 and mean_mass >= 1.0:
        return False, delta, "mass>=1 (illegal closure overflow)"
    if delta > env.max_delta_component:
        return False, delta, f"delta>{env.max_delta_component:.3f}"
    return True, delta, "ok"

