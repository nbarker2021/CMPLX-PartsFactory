# composed_from=38 witness=D:\PartsFactory\CMPLX-PartsFactory\staging\by-family\tarpit\cryptography\e6_tarpit_bridge.py
def torus_components_and_digits(v: np.ndarray, B: np.ndarray) -> Tuple[List[float], List[int]]:
    """
    Map v ∈ R^8 to torus components [cosθ0,sinθ0,...,cosθ3,sinθ3] and quantize each
    component into an oct digit (0..7).

    θi derived from paired coordinates after a seeded orthonormal change-of-basis:
      u = v B
      θi = atan2(u[2i+1], u[2i])
    """
    if v.shape != (8,):
        v = v.reshape(8,)
    u = v @ B
    comps: List[float] = []
    for i in range(0, 8, 2):
        theta = math.atan2(float(u[i + 1]), float(u[i]))
        comps.extend([math.cos(theta), math.sin(theta)])
    digits: List[int] = []
    for c in comps:
        # map [-1,1] -> [0,1] -> [0..7]
        q = int(math.floor(((c + 1.0) / 2.0) * 8.0))
        if q < 0:
            q = 0
        if q > 7:
            q = 7
        digits.append(q)
    return comps, digits

