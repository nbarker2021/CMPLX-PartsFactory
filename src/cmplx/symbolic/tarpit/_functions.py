# --- function: mirror_vector_for_torus ---
# composed_from=14 witness=D:\PartsFactory\CMPLX-PartsFactory\staging\by-family\tarpit\cryptography\e6_tarpit_bridge_v81.py
def mirror_vector_for_torus(eco: TarpitEcology,
                            base_v: np.ndarray,
                            *,
                            policy: str = "pole",
                            force_on_error: bool = False) -> Tuple[np.ndarray, Dict[str, Any]]:
    """
    Produce the "mirror lane" state vector used for torus8_mirror.

    Policies:
      - "pole":           mirror_v = -base_v
      - "mirroroperator": if any error wall exists, apply MirrorOperator to obtain a mediator vector;
                            else fall back to pole inversion.
      - "auto":           apply MirrorOperator only when the last error wall is mirror_candidate,
                            unless force_on_error=True.

    KNOWN LIMITATION (v8):
      MirrorOperator activation is gated exclusively on ErrorWall presence.
      Programs that halt cleanly (no errors) always receive pole inversion
      regardless of policy. To enable output-wall reflection, implement
      SurrogateErrorWall (derive ErrorWall from OutputWall signature) or
      OutputMirrorOperator (acts on OutputWall directly). See spec v8 §"next gap".

    Returns:
      (mirror_v, meta) where meta includes:
        - mirror_used: bool
        - mirror_source: "pole" | "mirroroperator"
        - last_error_class: str|None
        - last_error_mirror_candidate: bool
    """
    p = (policy or "pole").strip().lower()
    errors = list(getattr(eco.result, "error_walls", []) or [])
    last = errors[-1] if errors else None

    meta = {
        "mirror_used": False,
        "mirror_source": "pole",
        "last_error_class": getattr(getattr(last, "error_class", None), "value", None) if last else None,
        "last_error_mirror_candidate": bool(getattr(last, "mirror_candidate", False)) if last else False,
    }

    if p == "pole":
        return -base_v, meta

    if p not in ("mirroroperator", "auto"):
        raise ValueError(f"Unknown mirror policy: {policy!r}")

    if not last:
        return -base_v, meta

    should_apply = (p == "mirroroperator") or meta["last_error_mirror_candidate"] or bool(force_on_error)
    if not should_apply:
        return -base_v, meta

    grains = list(getattr(last, "reproducer_grains", []) or [])
    if not grains:
        gs = eco.field.grains.get(eco.field.pointer, [])
        grains = list(gs) if gs else collect_grains(eco)

    mo = MirrorOperator()
    mirrored = mo.apply_mirror(last, grains, time=int(getattr(eco, "step_count", 0) or 0))

    if mirrored and getattr(mirrored, "new_mediator", None) is not None:
        mv = mirrored.new_mediator.extent.vector
        meta["mirror_used"] = True
        meta["mirror_source"] = "mirroroperator"
        return np.array(mv, dtype=float), meta

    if mirrored and getattr(mirrored, "counter_grains", None):
        vecs = [g.extent.vector for g in mirrored.counter_grains
                if getattr(g, "extent", None) is not None and g.extent.vector is not None]
        if vecs:
            avg = np.mean(np.array(vecs, dtype=float), axis=0)
            # Guard: if counter-grain mean collapses to near-zero, fall back to pole
            if np.linalg.norm(avg) > 1e-10:
                meta["mirror_used"] = True
                meta["mirror_source"] = "mirroroperator"
                return avg, meta

    return -base_v, meta

# --- function: _edge_priority ---
# composed_from=14 witness=D:\PartsFactory\CMPLX-PartsFactory\staging\by-family\tarpit\cryptography\e6_tarpit_bridge_v8.py
def _edge_priority(surface_row: Dict[str, Any]) -> Tuple[int, int, int]:
    """
    Lower tuple is higher priority (stable ordering).
    We want:
      - higher event score first
      - earlier steps first (tie-break)
      - mirror-used True first (tie-break)
    """
    ev = int(surface_row.get("_event_score", 0))
    step = int(surface_row.get("step", 0))
    mirror_used = 0 if bool(surface_row.get("mirror_used", False)) else 1
    return (-ev, step, mirror_used)

# --- function: _run_score ---
# composed_from=22 witness=D:\PartsFactory\CMPLX-PartsFactory\staging\by-family\tarpit\cryptography\e6_tarpit_bridge_v8.py
def _run_score(summary: Dict[str, Any]) -> float:
    """
    Deterministic heuristic score for selecting "best" nodes.

    Higher is better.
    """
    s = 0.0
    if summary.get("halted"):
        s += 1.0
    if int(summary.get("n_error_walls", 0)) == 0:
        s += 1.0
    # Prefer "cleaner" walls (lower score indicates less residual)
    try:
        ws = float(summary.get("wall_score", 0.0))
        s += max(0.0, 1.0 - ws)
    except Exception:
        pass
    # Slight preference for more structured lattice, if present
    if str(summary.get("lattice")) == "2D_Grid":
        s += 0.25
    return float(s)

# --- function: detect_bounce_events ---
# composed_from=12 witness=D:\PartsFactory\CMPLX-PartsFactory\staging\by-family\tarpit\cryptography\e6_tarpit_bridge_v8.py
def detect_bounce_events(ledger: List[Dict[str, Any]],
                         *,
                         max_events: int = 8,
                         min_step_gap: int = 3) -> List[Dict[str, Any]]:
    """
    Detect "bounce surfaces" from a single run's ledger.

    Events are deterministic, derived from deltas in observables:
      - wall10 changed (proto-wall digits changed)
      - error wall count increased
      - envelope_ok became False
      - effective_dim changed

    Returns a *subset of ledger rows* (the surface rows), ordered by:
      (event_score desc, step asc), with a min step gap to avoid clustering.
    """
    if not ledger:
        return []

    events: List[Tuple[int, int, Dict[str, Any]]] = []  # (score, step, row)

    prev = ledger[0]
    for row in ledger[1:]:
        score = 0
        if str(row.get("wall10")) != str(prev.get("wall10")):
            score += 1
        if int(row.get("n_error_walls", 0)) > int(prev.get("n_error_walls", 0)):
            score += 2
        if (prev.get("envelope_ok", True) is True) and (row.get("envelope_ok", True) is False):
            score += 2
        if int(row.get("effective_dim", 0)) != int(prev.get("effective_dim", 0)):
            score += 1

        if score > 0:
            step = int(row.get("step", 0))
            r2 = dict(row)
            r2["_event_score"] = score
            events.append((score, step, r2))
        prev = row

    # Sort: strongest first, stable by step
    events.sort(key=lambda t: (-t[0], t[1]))

    picked: List[Dict[str, Any]] = []
    last_step = -10**9
    for score, step, row in events:
        if len(picked) >= int(max_events):
            break
        if step - last_step < int(min_step_gap):
            continue
        picked.append(row)
        last_step = step

    # If nothing triggered, return []
    return picked

# --- function: run_bounce_graph ---
# composed_from=14 witness=D:\PartsFactory\CMPLX-PartsFactory\staging\by-family\tarpit\cryptography\e6_tarpit_bridge_v81.py
def run_bounce_graph(program: str,
                     *,
                     depth: int = 3,
                     branch: int = 3,
                     max_nodes: int = 25,
                     surfaces: Optional[List[str]] = None,
                     bounce_mode: str = "torus-mirror",
                     bounce_len: int = 8,
                     keep_gauge: bool = True,
                     dimension: int = 8,
                     max_steps: int = 10_000,
                     mirror_policy: str = "pole",
                     force_mirror_on_error: bool = False,
                     envelope: Optional[RelativityEnvelope] = None,
                     store_ledgers: bool = True) -> Dict[str, Any]:
    """
    Branching traversal: a node is a program; edges are derived return programs computed
    from one of several surface rows ("final", "events", "frac", etc.).

    Deterministic constraints:
      - stable surface ordering (_edge_priority)
      - visited-set on program strings
      - bounded branch factor and max_nodes
    """
    codec = E6Codec()
    if envelope is None:
        envelope = RelativityEnvelope(enabled=False)

    if surfaces is None:
        surfaces = ["final", "events"]

    depth = max(0, int(depth))
    branch = max(1, int(branch))
    max_nodes = max(1, int(max_nodes))

    gauge_seed = seed_from_program(program, dimension) if keep_gauge else None

    nodes: List[Dict[str, Any]] = []
    edges: List[Dict[str, Any]] = []
    visited = set()

    # Node 0
    queue: List[Tuple[int, int]] = []  # (node_id, depth)
    def add_node(prog: str, parent: Optional[int], via: Optional[Dict[str, Any]], d: int) -> int:
        out = run_etp_with_ledger(
            prog,
            dimension=dimension,
            max_steps=max_steps,
            gauge_seed=gauge_seed,
            envelope=envelope,
            mirror_policy=mirror_policy,
            force_mirror_on_error=force_mirror_on_error,
        )
        node_id = len(nodes)
        node = {
            "node_id": node_id,
            "depth": d,
            "program": prog,
            "summary": out["summary"],
        }
        # Always store ledger during traversal (needed for surface computation);
        # strip from output at the end if store_ledgers=False
        node["ledger"] = out["ledger"]
        # record the best surface row for compact summary
        if out["ledger"]:
            best_row = select_bounce_source_row(out["ledger"], bounce_from="events") if "events" in surfaces else out["ledger"][-1]
            node["surface_row"] = {k: best_row.get(k) for k in (
                "step", "wall10", "torus8", "torus8_mirror",
                "n_error_walls", "last_error_class",
                "mirror_used", "mirror_source",
                "envelope_ok", "envelope_delta",
                "effective_dim", "gram5",
            )}
        node["score"] = _run_score(out["summary"])
        nodes.append(node)

        if parent is not None and via is not None:
            edges.append({
                "src": parent,
                "dst": node_id,
                **via,
            })
        queue.append((node_id, d))
        return node_id

    add_node(program, parent=None, via=None, d=0)
    visited.add(program)

    # BFS by depth
    qi = 0
    while qi < len(queue):
        node_id, d = queue[qi]
        qi += 1
        if d >= depth:
            continue
        if len(nodes) >= max_nodes:
            break

        ledger = nodes[node_id].get("ledger", [])
        if not ledger:
            continue

        # candidate surfaces
        surf_rows = select_surface_rows_for_graph(ledger, surfaces=surfaces, max_events=8)
        # stable ordering
        surf_rows.sort(key=_edge_priority)

        # generate edges
        out_edges = 0
        for sr in surf_rows:
            if out_edges >= branch:
                break
            toks = derive_return_tokens(sr, mode=bounce_mode, length=bounce_len)
            next_prog = codec.tokens_to_etp_program(toks, use_repeats=True)
            if next_prog in visited:
                continue
            if len(nodes) >= max_nodes:
                break

            via = {
                "surface_kind": sr.get("_surface_kind", "unknown"),
                "surface_step": int(sr.get("step", 0)),
                "bounce_mode": bounce_mode,
                "bounce_len": int(bounce_len),
                "derived_return_tokens": [t.to_dict() for t in toks],
            }
            add_node(next_prog, parent=node_id, via=via, d=d + 1)
            visited.add(next_prog)
            out_edges += 1

    # choose best node + reconstruct best path via parent edges
    # Secondary key: lower node_id wins ties (earlier = more ancestral)
    best = max(nodes, key=lambda n: (float(n.get("score", 0.0)), -int(n["node_id"])))
    best_id = int(best["node_id"])

    # Build parent map from edges (first edge to a dst is its parent)
    parent_of = {}
    edge_of = {}
    for e in edges:
        dst = int(e["dst"])
        if dst not in parent_of:
            parent_of[dst] = int(e["src"])
            edge_of[dst] = e

    path = [best_id]
    cur = best_id
    while cur in parent_of:
        cur = parent_of[cur]
        path.append(cur)
    path.reverse()

    # Strip ledgers from output if not requested (they were needed for traversal)
    if not store_ledgers:
        for n in nodes:
            n.pop("ledger", None)

    return {
        "meta": {
            "initial_program": program,
            "depth": depth,
            "branch": branch,
            "max_nodes": max_nodes,
            "surfaces": surfaces,
            "bounce_mode": bounce_mode,
            "bounce_len": bounce_len,
            "keep_gauge": keep_gauge,
            "mirror_policy": mirror_policy,
            "force_mirror_on_error": bool(force_mirror_on_error),
            "dimension": dimension,
            "max_steps": max_steps,
        },
        "nodes": nodes,
        "edges": edges,
        "best_node_id": best_id,
        "best_path": path,
    }

# --- function: select_surface_rows_for_graph ---
# composed_from=14 witness=D:\PartsFactory\CMPLX-PartsFactory\staging\by-family\tarpit\cryptography\e6_tarpit_bridge_v8.py
def select_surface_rows_for_graph(ledger: List[Dict[str, Any]],
                                  *,
                                  surfaces: List[str],
                                  max_events: int = 8) -> List[Dict[str, Any]]:
    """
    Return a list of candidate surface rows for graph branching.

    surfaces can include: "final", "events", "frac"
      - final  -> ledger[-1]
      - events -> detect_bounce_events(...), up to max_events
      - frac   -> rows at 0.25, 0.50, 0.75

    Each returned row has a stable label in row["_surface_kind"].
    """
    if not ledger:
        return []

    out: List[Dict[str, Any]] = []
    want = {s.strip().lower() for s in (surfaces or ["final"])}

    def add_row(row: Dict[str, Any], kind: str):
        r2 = dict(row)
        r2["_surface_kind"] = kind
        out.append(r2)

    if "final" in want:
        add_row(ledger[-1], "final")

    if "frac" in want and len(ledger) >= 4:
        for f in (0.25, 0.50, 0.75):
            idx = int(round(f * (len(ledger) - 1)))
            add_row(ledger[idx], f"frac:{f:.2f}")

    if "events" in want:
        ev = detect_bounce_events(ledger, max_events=max_events)
        for i, r in enumerate(ev):
            add_row(r, f"event:{i}")

    # Deduplicate by (step, kind) while preserving order
    seen = set()
    dedup: List[Dict[str, Any]] = []
    for r in out:
        key = (int(r.get("step", -1)), str(r.get("_surface_kind")))
        if key in seen:
            continue
        seen.add(key)
        dedup.append(r)
    return dedup
    if s.startswith("frac:"):
        try:
            frac = float(s.split(":", 1)[1])
            frac = max(0.0, min(1.0, frac))
            idx = int(round(frac * (len(ledger) - 1)))
            return ledger[idx]
        except Exception:
            return ledger[-1]

    if s.startswith("step:"):
        try:
            target = int(s.split(":", 1)[1])
            # find closest by absolute difference
            best = min(ledger, key=lambda r: abs(int(r.get("step", 0)) - target))
            return best
        except Exception:
            return ledger[-1]

    # Unknown -> fall back
    return ledger[-1]

