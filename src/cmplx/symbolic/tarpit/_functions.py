def derive_return_tokens(final_row: Dict[str, Any],
                         *,
                         mode: str = "torus-mirror",
                         length: int = 8) -> List[E6Token]:
    """
    Deterministically derive a *return* action stream (E6 tokens) from the final
    observed state of a run.

    This is the minimal implementation of:
      - "return actions can be encoded in a second 8D block"
      - "walls can bounce multiple times"

    Inputs
    ------
    final_row: last ledger row from run_etp_with_ledger()

    Modes
    -----
    - torus-mirror:
        op  = torus8_mirror[i]
        rep = torus8[i]
    - wall10:
        op  = wall_digits[i] mod 8
        rep = torus8_mirror[i]
    - hybrid:
        op  = (torus8_mirror[i] XOR wall_digits[i]) mod 8
        rep = (torus8[i] + wall_digits[i]) mod 8

    Output
    ------
    List[E6Token] of length `length`, with t=0..length-1.
    """
    mode = (mode or "torus-mirror").lower().strip()
    torus = list(final_row.get("torus8", [])) or [0] * 8
    mirror = list(final_row.get("torus8_mirror", [])) or [0] * 8
    wall_digits = _parse_wall10_digits(str(final_row.get("wall10", ""))) or [0, 0, 0]

    toks: List[E6Token] = []
    n = max(1, int(length))
    for i in range(n):
        a = torus[i % len(torus)]
        b = mirror[i % len(mirror)]
        w = wall_digits[i % len(wall_digits)]

        if mode == "torus-mirror":
            op = int(b) & 7
            rep = int(a) & 7
        elif mode == "wall10":
            op = int(w) % 8
            rep = int(b) & 7
        elif mode == "hybrid":
            op = (int(b) ^ int(w)) % 8
            rep = (int(a) + int(w)) % 8
        else:
            raise ValueError(f"Unknown bounce mode: {mode!r}")

        toks.append(E6Token(t=i, bits6=merge_from_bf(op, rep), meta={
            "derived_from": "final_row",
            "mode": mode,
            "i": i,
            "op": op,
            "rep": rep + 1,  # repeats in ETP program
        }))
    return toks

