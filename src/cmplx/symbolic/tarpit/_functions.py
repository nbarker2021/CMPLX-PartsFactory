# composed_from=20 witness=D:\PartsFactory\CMPLX-PartsFactory\staging\by-family\tarpit\cryptography\e6_tarpit_bridge_v8.py
def select_bounce_source_row(ledger: List[Dict[str, Any]], *, bounce_from: str = "final") -> Dict[str, Any]:
    """
    Select which ledger row to use as the "bounce surface".

    bounce_from formats:
      - "final"                 -> last row
      - "frac:0.50"             -> row at fraction of ledger length
      - "step:123"              -> row whose 'step' equals 123 (or nearest)
      - "events"                -> best detected event row (or final if none)
      - "events:first|last|k"   -> choose among detected events (k = 0-based index)

    This supports bounces off intermediate "wall events" without manual step selection.
    """
    if not ledger:
        return {}

    s = (bounce_from or "final").strip().lower()

    if s == "final":
        return ledger[-1]

    if s.startswith("frac:"):
        try:
            frac = float(s.split(":", 1)[1])
            frac = max(0.0, min(1.0, frac))
            idx = int(round(frac * (len(ledger) - 1)))
            return ledger[idx]
        except Exception:
            return ledger[-1]

    if s.startswith("step:"):
        try:
            target = int(s.split(":", 1)[1])
            best = min(ledger, key=lambda r: abs(int(r.get("step", 0)) - target))
            return best
        except Exception:
            return ledger[-1]

    if s.startswith("events"):
        ev = detect_bounce_events(ledger)
        if not ev:
            return ledger[-1]
        if s == "events":
            return ev[0]
        if ":" in s:
            tail = s.split(":", 1)[1].strip()
            if tail == "first":
                return ev[0]
            if tail == "last":
                return ev[-1]
            try:
                k = int(tail)
                if 0 <= k < len(ev):
                    return ev[k]
            except Exception:
                pass
        return ev[0]

    return ledger[-1]

