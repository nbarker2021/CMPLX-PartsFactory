def run_bounce_series(program: str,
                      *,
                      bounces: int = 1,
                      bounce_mode: str = "torus-mirror",
                      bounce_len: int = 8,
                      bounce_from: str = "final",
                      keep_gauge: bool = True,
                      dimension: int = 8,
                      max_steps: int = 10_000,
                      mirror_policy: str = "pole",
                      force_mirror_on_error: bool = False,
                      envelope: Optional[RelativityEnvelope] = None) -> Dict[str, Any]:
    """
    Run an initial program, then repeatedly derive a return-program from the
    previous run's final state, for `bounces` reflections.

    Interpretation:
      - bounces=0 -> just run the initial program
      - bounces=1 -> initial run, then 1 derived return run
      - bounces=k -> initial run + k derived return runs

    keep_gauge=True keeps the torus basis seed fixed to the initial program,
    so torus digits are comparable across bounces ("same space").
    """
    codec = E6Codec()
    bounces = max(0, int(bounces))

    if envelope is None:
        envelope = RelativityEnvelope(enabled=False)

    gauge_seed = seed_from_program(program, dimension) if keep_gauge else None

    runs: List[Dict[str, Any]] = []
    cur_program = program

    for r in range(bounces + 1):
        out = run_etp_with_ledger(
            cur_program,
            dimension=dimension,
            max_steps=max_steps,
            gauge_seed=gauge_seed,
            envelope=envelope,
            mirror_policy=mirror_policy,
            force_mirror_on_error=force_mirror_on_error,
        )

        final_row = select_bounce_source_row(out["ledger"], bounce_from=bounce_from) if out["ledger"] else {}
        run_entry: Dict[str, Any] = {
            "run_index": r,
            "input_program": cur_program,
            "summary": out["summary"],
            "final_row": {
                "wall10": final_row.get("wall10"),
                "torus8": final_row.get("torus8"),
                "torus8_mirror": final_row.get("torus8_mirror"),
                "effective_dim": final_row.get("effective_dim"),
                "gram5": final_row.get("gram5"),
                "envelope_ok": final_row.get("envelope_ok"),
                "envelope_delta": final_row.get("envelope_delta"),
            },
            "ledger": out["ledger"],
        }

        if r < bounces and out["ledger"]:
            toks = derive_return_tokens(final_row, mode=bounce_mode, length=bounce_len)
            next_program = codec.tokens_to_etp_program(toks, use_repeats=True)
            run_entry["derived_return_program"] = next_program
            run_entry["derived_return_tokens"] = [t.to_dict() for t in toks]
            cur_program = next_program

        runs.append(run_entry)

    return {
        "initial_program": program,
        "bounces": bounces,
        "bounce_mode": bounce_mode,
        "bounce_len": bounce_len,
        "bounce_from": bounce_from,
        "keep_gauge": keep_gauge,
        "mirror_policy": mirror_policy,
        "force_mirror_on_error": bool(force_mirror_on_error),
        "dimension": dimension,
        "max_steps": max_steps,
        "runs": runs,
    }

