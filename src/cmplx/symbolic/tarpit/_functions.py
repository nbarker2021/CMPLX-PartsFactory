def demo_vocabulary_evolution():
    """Show how vocabulary evolves through composition."""
    print("\n" + "=" * 70)
    print("DEMO 8: Vocabulary Evolution")
    print("=" * 70)
    
    vocab = TokenVocabulary.create_basic_vocabulary(dimension=64)
    initial_size = len(vocab.tokens)
    
    print(f"\nInitial vocabulary size: {initial_size}")
    
    # Run multiple composition programs
    programs = [
        "black } bird",
        "fast } bird", 
        "bird } fly",
        "black } dog",
    ]
    
    novel_products = set()
    
    for prog in programs:
        ecology = SemanticEcology(vocabulary=vocab)
        wall = ecology.run(prog)
        
        for dust in ecology.bond_engine.dust_history:
            if dust.is_novel:
                novel_products.add(dust.composite.token)
    
    print(f"\nNovel compositions discovered:")
    for product in sorted(novel_products):
        print(f"  {product}")
    
    print(f"\nVocabulary growth: {initial_size} -> {initial_size + len(novel_products)}")
    print(f"Expansion ratio: {len(novel_products)/initial_size:.2f}x")
