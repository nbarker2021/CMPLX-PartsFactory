def gram_effective_dim(grain_vectors: List[np.ndarray], take: int = 10) -> Tuple[int, List[float]]:
    vectors = [v for v in grain_vectors if v is not None]
    if len(vectors) < 2:
        return len(vectors), [0.0]
    V = np.array(vectors[:take], dtype=float)
    G = V @ V.T
    ev = sorted(np.linalg.eigvalsh(G).tolist(), reverse=True)
    eff = sum(1 for x in ev if x > 1e-6)
    return eff, ev[:5]

