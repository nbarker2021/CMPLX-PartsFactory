        cursor.execute('\n            SELECT steps, confidence FROM reasoning_chains\n            WHERE chain_id = ?\n        ', (chain_id,))
        row = cursor.fetchone()
        conn.close()
        if not row:
            return {'valid': False, 'error': 'Chain not found'}
        steps = json.loads(row[0])
        confidence = row[1]
        valid_steps = 0
        for step in steps:
            if step.get('confidence', 0) > 0.7:
                valid_steps += 1
        is_valid = valid_steps == len(steps) and confidence > 0.8
        return {'chain_id': chain_id, 'valid': is_valid, 'steps_valid': valid_steps, 'total_steps': len(steps), 'confidence': confidence}

