    def _are_contradictory(self, stmt1: str, stmt2: str) -> bool:
        """Check if two statements are contradictory."""
        negations = ['not ', 'no ', 'false', 'invalid']
        stmt1_lower = stmt1.lower()
        stmt2_lower = stmt2.lower()
        for neg in negations:
            if neg in stmt1_lower and stmt1_lower.replace(neg, '') in stmt2_lower:
                return True
            if neg in stmt2_lower and stmt2_lower.replace(neg, '') in stmt1_lower:
                return True
        return False

    def _build_inline(self, points):
        """Build InlineHasher from points with sector-based metadata"""
        points = np.asarray(points, dtype=float)
        ih = InlineHasher(dim=points.shape[1], grid=float(self.cfg.get('inline_grid', 32.0)), proj_dims=int(self.cfg.get('inline_proj_dims', 8)), seed=int(self.cfg.get('inline_seed', 17)))
        n = points.shape[0]
        center = points.mean(axis=0)
        vecs = points - center
        angles = np.arctan2(vecs[:, 1], vecs[:, 0])
        num_floors = self.cfg.get('num_sectors', 32)
        sec_edges = np.linspace(-np.pi, np.pi, num_floors + 1)
        sectors = np.digitize(angles, sec_edges) - 1
        for i in range(n):
            meta = {'building': self.cfg.get('building', 'default'), 'floor': f'F{sectors[i]}', 'room': 'R0'}
            ih.insert(i, points[i], meta)
        for i in range(n):
            ih.bump_heat([i])
        dual_export = ih.export_dualwrite()
        for i, h in dual_export['heat'].items():
            self.dual.heat[i] += h
        for a, b, w in dual_export['edges']:
            self.dual.record_edge_weight(a, b, w)
        return ih

