def produce(econ: EconomyState, recipe: Dict[str, float], out: Dict[str, float]) -> Dict[str, float]:
    """Consume capacity tokens (recipe) to produce other capacity tokens (out).

    This is a stand-in for internal scheduler transforms:
    - spending cpu+ram produces tokens
    - spending gpu+ram produces embed_ops
    - spending cpu+io produces hash_ops
    - spending io+net produces store_ops

    Output is bottlenecked by the scarcest input.
    """
    if not recipe:
        for k, v in out.items():
            econ.resources[k] = econ.resources.get(k, 0.0) + float(v)
        return {'bottleneck': None, 'scale': 1.0}
    scales = []
    keys = list(recipe.keys())
    for k, need in recipe.items():
        have = econ.resources.get(k, 0.0)
        scales.append(1.0 if need <= 0 else have / need)
    scale = max(0.0, min(scales))
    for k, need in recipe.items():
        econ.resources[k] = max(0.0, econ.resources.get(k, 0.0) - need * scale)
    for k, make in out.items():
        econ.resources[k] = econ.resources.get(k, 0.0) + make * scale
    bn = None
    if scales:
        idx = scales.index(min(scales))
        bn = keys[idx]
    return {'bottleneck': bn, 'scale': scale}

def repay(bs: BalanceSheet, amount: float) -> float:
    if bs.in_default:
        return 0.0
    pay = min(bs.debt, max(0.0, amount))
    bs.debt -= pay
    return pay

@app.post('/stake')
def stake(req: StakeRequest):
    """Stake coins for yield."""
    pool = STAKING_POOLS.get(req.pool)
    if not pool:
        raise HTTPException(404, f"Pool '{req.pool}' not found. Available: {list(STAKING_POOLS.keys())}")
    if req.amount < pool['min_stake']:
        raise HTTPException(400, f"Minimum stake for {req.pool}: {pool['min_stake']}")
    _adjust_balance(req.agent_id, -req.amount, f'stake:{req.pool}')
    stake_id = f'stk-{uuid.uuid4().hex[:12]}'
    current_apy = _compute_dynamic_apy(req.pool)
    stake_record = {'stake_id': stake_id, 'agent_id': req.agent_id, 'pool': req.pool, 'amount': req.amount, 'apy_at_entry': current_apy, 'staked_at': _now_iso(), 'estimated_annual_yield': round(req.amount * current_apy, 4)}
    stakes[stake_id] = stake_record
    _pg_save_stake(stake_record)
    pool['total_staked'] += req.amount
    return {'stake_id': stake_id, 'pool': req.pool, 'amount': req.amount, 'apy': current_apy, 'estimated_annual_yield': stake_record['estimated_annual_yield']}

