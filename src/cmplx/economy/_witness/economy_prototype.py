def equilibrium_index(workspace: Path, asset: str='MDHG') -> Dict[str, Any]:
    ensure_economy(workspace)
    p = workspace / 'economy' / 'ledger.jsonl'
    total_mint = 0.0
    total_burn = 0.0
    sink = 0.0
    planets = set()
    with p.open('r', encoding='utf-8') as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                rec = json.loads(line)
            except Exception:
                continue
            if rec.get('asset') != asset:
                continue
            t = rec.get('type')
            amt = float(rec.get('amount', 0.0))
            if t == 'mint':
                total_mint += amt
            if t == 'burn':
                total_burn += amt
            if rec.get('to') == '__sink__':
                sink += amt
            for who in (rec.get('to'), rec.get('from')):
                if who and who not in ('__sink__', None):
                    planets.add(who)
    supply = total_mint - total_burn
    pressure = sink / max(supply, 1.0)
    return {'asset': asset, 'total_supply': supply, 'sink': sink, 'pressure': pressure, 'active_planets': len(planets)}

def gini(values: List[float]) -> float:
    if not values:
        return 0.0
    v = sorted((max(0.0, float(x)) for x in values))
    n = len(v)
    s = sum(v)
    if s <= 0:
        return 0.0
    cum = 0.0
    for i, x in enumerate(v, start=1):
        cum += i * x
    return 2 * cum / (n * s) - (n + 1) / n

