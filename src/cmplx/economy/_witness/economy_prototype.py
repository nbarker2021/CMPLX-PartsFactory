def borrow(bs: BalanceSheet, amount: float) -> float:
    if bs.in_default:
        return 0.0
    headroom = max(0.0, bs.credit_limit - bs.debt)
    take = min(headroom, max(0.0, amount))
    bs.debt += take
    return take

