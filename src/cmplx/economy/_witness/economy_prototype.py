def tick():
    """Daemon tick — auto-create listings from high-quality PG atoms, accrue staking yields."""
    created_listings = 0
    yields_paid = 0
    conn = _get_pg()
    if conn and len(listings) < 50:
        try:
            with conn.cursor() as cur:
                cur.execute('SELECT atom_id, quality_score, LEFT(content, 100) as preview\n                    FROM atoms WHERE quality_score > 0.7\n                    ORDER BY quality_score DESC LIMIT 5')
                for atom_id, qscore, preview in cur.fetchall():
                    lid = f'lst-{atom_id[:12]}'
                    if lid not in listings:
                        listings[lid] = {'listing_id': lid, 'item_type': 'atom', 'item_id': atom_id, 'seller_id': 'system', 'price': round(qscore * 10, 2), 'description': preview or atom_id, 'status': 'active', 'created_at': _now_iso(), 'snap_labels': ['economy.auto_listing']}
                        _pg_save_listing(listings[lid])
                        created_listings += 1
        except:
            pass
    for sid, stake in list(stakes.items()):
        if stake.get('status') == 'active':
            pool = STAKING_POOLS.get(stake.get('pool', ''))
            if pool:
                apy = _compute_dynamic_apy(stake['pool'])
                hourly_yield = stake.get('amount', 0) * apy / (365 * 24)
                if hourly_yield > 0.001:
                    _adjust_balance(stake['staker_id'], hourly_yield, f"staking_yield:{stake['pool']}:{sid[:8]}")
                    yields_paid += 1
    auto_buys = 0
    active_listings = [l for l in listings.values() if l.get('status') == 'active']
    system_buyers = ['pipeline', 'manny', 'claudeclaw', 'engine-0']
    for buyer in system_buyers:
        balance = _get_balance(buyer)
        for listing in active_listings[:2]:
            if listing['status'] == 'active' and listing['price'] <= balance * 0.1:
                try:
                    _adjust_balance(buyer, -listing['price'], f"buy:{listing['listing_id']}")
                    _adjust_balance(listing['seller_id'], listing['price'] * 0.9, f"sell:{listing['listing_id']}")
                    listing['status'] = 'sold'
                    listing['buyer_id'] = buyer
                    auto_buys += 1
                except:
                    pass
                break
    auto_staked = 0
    for agent_id in system_buyers:
        balance = _get_balance(agent_id)
        if balance > 500 and (not any((s.get('staker_id') == agent_id for s in stakes.values()))):
            stake_amount = balance * 0.2
            sid = f'stake-{agent_id}-{uuid.uuid4().hex[:6]}'
            stakes[sid] = {'stake_id': sid, 'staker_id': agent_id, 'pool': 'stability', 'amount': stake_amount, 'status': 'active', 'created_at': _now_iso()}
            _adjust_balance(agent_id, -stake_amount, f'stake:{sid}')
            _pg_save_stake(stakes[sid])
            auto_staked += 1
    return {'ok': True, 'listings_created': created_listings, 'yields_paid': yields_paid, 'auto_buys': auto_buys, 'auto_staked': auto_staked, 'total_listings': len(listings), 'total_stakes': len(stakes)}

