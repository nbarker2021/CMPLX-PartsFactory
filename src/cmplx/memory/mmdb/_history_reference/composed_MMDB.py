        if label:
            atoms = self._atoms.by_label(label, limit=limit)
        elif crt_channel >= 0:
            atoms = self._atoms.by_crt_channel(crt_channel, limit=limit)
        elif floor:
            atoms = self._atoms.by_floor(floor, limit=limit, offset=offset)
        elif dr:
            atoms = self._atoms.by_dr(dr, limit=limit)
        else:
            atoms = self._atoms.sample(n=limit, min_quality=min_quality)
        if floor and (not label) and (not crt_channel >= 0):
            pass
        if dr and floor:
            atoms = [a for a in atoms if a.digital_root == dr]
        if min_quality > 0:
            atoms = [a for a in atoms if a.quality_overall >= min_quality]
        return [MMDBResult(atom=a, score=1.0, rank=i + 1, search_type='property') for i, a in enumerate(atoms)]

