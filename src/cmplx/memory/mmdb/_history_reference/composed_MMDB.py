    def query_nearest_e8(self, coords: np.ndarray, limit: int=10) -> List[Tuple[Atom, float]]:
        """
        Find atoms nearest to given E8 coordinates.
        Uses cosine similarity for efficiency (exact lattice distance
        requires loading all atoms).
        """
        target = np.asarray(coords, dtype=np.float64)[:8]
        target_norm = np.linalg.norm(target)
        if target_norm < 1e-10:
            return []
        rows = self._conn.execute("SELECT atom_id, e8_coords, data_json FROM atoms WHERE phase != 'tombstoned'").fetchall()
        scored = []
        for row in rows:
            e8 = np.array(json.loads(row[1]), dtype=np.float64)
            e8_norm = np.linalg.norm(e8)
            if e8_norm < 1e-10:
                continue
            similarity = float(np.dot(target, e8) / (target_norm * e8_norm))
            scored.append((similarity, row[2]))
        scored.sort(key=lambda x: -x[0])
        return [(Atom.from_dict(json.loads(data)), sim) for sim, data in scored[:limit]]

