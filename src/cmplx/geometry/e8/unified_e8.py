class EvidenceSurface:
    """
    Treat each snap as an evidence surface: a small perturbation can flip many coupled binary states.
    We model this with an Ising-like energy:  Phi(s) = -0.5 s^T J s - h^T s,  s in {-1,+1}^M (masked where unknown).
    Variables:
      - s_i: sign of each of 8 coordinates (0 abstains)
      - nz_i: nonzero indicator of each coord (maps to field, not a spin here)
      - coset_half: +1 if half-integers (Z+1/2-even), -1 if integers (Z-even)
    Couplings:
      - sign-sign: pairwise couplings derived from E8 root combinatorics (pairs appear in (±1,±1,0^6) roots).
      - coset-sign: weak coupling encouraging half-integers to decorrelate signs; integers to align pairs.
    External field:
      - from residual direction and magnitude: pushes spins to align with residual.
    """
    def __init__(self, lambda_pair: float = 0.2, lambda_coset: float = 0.05, lambda_residual: float = 0.8):
        self.lambda_pair = float(lambda_pair)
        self.lambda_coset = float(lambda_coset)
        self.lambda_residual = float(lambda_residual)

    @staticmethod
    def _pair_matrix():
        # In E8, "type I" roots have two ±1 and six zeros. For any i<j, there are 4 sign combos.
        # Use a normalized adjacency where each distinct pair contributes equally.
        A = np.ones((8,8), dtype=float) - np.eye(8)
        A *= 4.0  # count of sign combos
        # Normalize by max degree (7*4) so entries in [0,1]
        A /= (7.0*4.0)
        return A

    @staticmethod
    def _torus_closure(theta):
        """
        Simple octet closure score on the 8-torus: penalize phases far from multiples of pi/4.
        theta: 8-vector of angles in radians (0..2pi)
        Returns small when phases are near 8-ary grid.
        """
        target = (np.pi/4.0) * np.round(theta / (np.pi/4.0))
        return float(np.linalg.norm(np.angle(np.exp(1j*(theta - target)))))

    def build_problem(self, meta: Dict[str, Any]) -> Dict[str, Any]:
        x = np.asarray(meta["snapped"], dtype=float)
        r = np.asarray(meta["residual"], dtype=float)
        coset = meta.get("coset","other")
        # spins for signs
        s = np.array([sign_bin(v) for v in x], dtype=int)  # {-1,0,+1}
        mask = (s != 0)
        s[~mask] = 1  # fill to +1 where abstaining; masked in updates
        # fields
        h = np.zeros(9, dtype=float)  # 8 sign spins + 1 coset spin
        # residual field pushes sign toward sign of r (if residual has structure)
        h[:8] = self.lambda_residual * np.tanh(r)  # soft field from residual
        # coset spin: +1 half, -1 integer
        c = 1.0 if coset == "Z+1/2-even" else -1.0 if coset == "Z-even" else 0.0
        # Prefer stability of observed coset
        h[8] = 0.25 * c

        # Coupling matrix J (9x9)
        J = np.zeros((9,9), dtype=float)
        A = self._pair_matrix()
        J[:8,:8] = self.lambda_pair * A
        # coset-sign weak coupling: half-integer encourages alternating signs (penalize same sign pairs)
        # We encode as negative couplings to net magnetization of signs.
        for i in range(8):
            J[i,8] = J[8,i] = -self.lambda_coset / 8.0

        # torus closure score (diagnostic)
        theta = (2.0*np.pi) * (x - np.floor(x))  # fractional phases on T^8
        closure = self._torus_closure(theta)

        return dict(x=x, r=r, s0=s.copy(), mask=mask, J=J, h=h, coset_spin=c, closure=closure)

    @staticmethod
    def _energy(s_all, J, h):
        return float(-0.5 * s_all @ (J @ s_all) - h @ s_all)

    def descend(self, problem, max_iter=64):
        s = problem["s0"].astype(int)
        mask = problem["mask"]
        J = problem["J"]; h = problem["h"]
        # include coset spin as last component
        c = 1 if problem["coset_spin"] >= 0 else -1
        s_full = np.concatenate([s, np.array([c], dtype=int)])
        # masked spins do not flip (where original sign=0)
        flip_mask = np.concatenate([mask, np.array([True])])

        history = []
        E = self._energy(s_full, J, h)
        history.append(dict(E=E, s=s_full.copy()))
        improved = True
        it = 0
        while improved and it < max_iter:
            improved = False
            it += 1
            # try single-spin flips greedy
            for i in range(9):
                if not flip_mask[i]: continue
                s_try = s_full.copy()
                s_try[i] *= -1
                E_try = self._energy(s_try, J, h)
                if E_try + 1e-12 < E:
                    s_full = s_try; E = E_try; improved = True
                    history.append(dict(E=E, s=s_full.copy(), flip=i))
        return dict(s_final=s_full, E=E, steps=len(history)-1, history=history)


