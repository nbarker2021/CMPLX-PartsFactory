def analyze_snap_v5(meta: Dict[str,Any],
                    lambda_pair: float=0.6,
                    lambda_coset: float=0.2,
                    lambda_residual: float=0.8,
                    lambda_crt2: float=0.3,
                    lambda_crt4: float=0.2,
                    max_flips: int=128) -> Dict[str,Any]:
    y = meta["snapped"]; r = meta["residual"]; B = meta["basis"]
    n = y.shape[0]
    # Spins: signs + crt residues
    s_sign, m_sign = _sign_spins(y)
    s2, s4 = _crt_spins(y)
    # External fields
    h_sign = _weyl_invariant_field(r, B)[:n]
    h_sign = np.pad(h_sign, (0, max(0, n-len(h_sign))), constant_values=0.0)
    # Build simple pairwise couplings: nearest-neighbor chain + weak all-to-all mean-field
    J = np.zeros((n, n))
    for i in range(n-1):
        J[i,i+1] += lambda_pair
        J[i+1,i] += lambda_pair
    J += (lambda_pair*0.05) * (np.ones((n,n)) - np.eye(n))

    # Coset bias: prefer coherent sign alignment or anti-alignment
    cos = meta.get("coset","other")
    if cos == "Z-even":
        h_sign += lambda_coset * np.sign(y)
    elif cos == "Z+1/2-even":
        h_sign -= lambda_coset * np.sign(y)

    # CRT fields
    h2 = lambda_crt2 * s2.astype(float)
    h4 = lambda_crt4 * s4.astype(float)
    h = lambda_residual * h_sign + h2 + h4

    # Energy and greedy flips on sign spins only (CRT spins are "fields")
    def energy(s):
        return float(-0.5 * s @ (J @ s) - h @ s)

    s = s_sign.copy()
    E0 = energy(s)
    flips = []
    for _ in range(max_flips):
        # compute gain for flipping each spin
        gains = []
        for i in range(n):
            if m_sign[i] == 0:  # abstain if zero magnitude coordinate
                gains.append((-np.inf, i))
                continue
            s_new = s.copy()
            s_new[i] = -s_new[i] if s_new[i]!=0 else 0
            gains.append((E0 - energy(s_new), i))
        # choose best positive gain
        gains.sort(reverse=True, key=lambda x:x[0])
        if gains[0][0] > 1e-12:
            _, i = gains[0]
            s[i] = -s[i]
            flips.append(int(i))
            E0 = energy(s)
        else:
            break

    closure = _rational_closure_score(y)
    return {
        "final_signs": s.tolist(),
        "flips": flips,
        "energy": E0,
        "closure": closure,
        "coset": cos,
        "crt2_balance": float(np.mean(s2)),
        "crt4_balance": float(np.mean(s4))
    }


