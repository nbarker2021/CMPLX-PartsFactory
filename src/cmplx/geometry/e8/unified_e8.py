def _rational_closure_score(vec: np.ndarray, denoms=(2,3,4,6,8,10)):
    # Measure how close fractional parts are to rationals with given denominators
    f = np.modf(vec)[0]
    f = np.where(f<0, f+1.0, f)
    best = 0.0
    for d in denoms:
        q = np.round(f * d)/d
        err = np.mean(np.abs(f - q))
        sc = 1.0/(1.0+err)  # higher is better
        if sc > best:
            best = sc
    return float(best)


