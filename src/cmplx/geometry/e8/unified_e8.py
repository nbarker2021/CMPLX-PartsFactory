def _nullspace_mod2(H):
    import numpy as np
    H = np.array(H) % 2
    m, n = H.shape
    H = H.copy()
    pivots = []
    row = 0
    for col in range(n):
        pivot = None
        for r in range(row, m):
            if H[r, col] == 1:
                pivot = r; break
        if pivot is None: 
            continue
        if pivot != row:
            H[[row, pivot]] = H[[pivot, row]]
        for r in range(m):
            if r != row and H[r,col] == 1:
                H[r,:] ^= H[row,:]
        pivots.append(col)
        row += 1
        if row==m: break
    free = [c for c in range(n) if c not in pivots]
    G = []
    for f in free:
        v = np.zeros(n, dtype=int)
        v[f] = 1
        H_rref = H.copy()
        pr = 0
        for col in range(n):
            if pr < len(pivots) and pivots[pr] == col:
                s = 0
                for c2 in range(col+1, n):
                    s ^= (H_rref[pr, c2] & v[c2])
                v[col] = s
                pr += 1
        G.append(v % 2)
    if not G:
        return np.zeros((0,n), dtype=int)
    G = np.array(G, dtype=int) % 2
    return G


