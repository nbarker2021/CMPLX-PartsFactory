class Tier1:
    proj: np.ndarray  # shape (B, 8)
    def signature(self, X: np.ndarray) -> np.ndarray:
        S = (X @ self.proj.T) >= 0.0
        # pack bits into integers (e.g., 16-bit if B<=16)
        out = np.zeros((X.shape[0],), dtype=np.uint32)
        B = self.proj.shape[0]
        for i in range(B):
            out |= (S[:, i].astype(np.uint32) << i)
        return out


