    for i in range(0, max(0, len(toks)-W+1), S):
        window = toks[i:i+W]
        dims = [float(len(t)) for t in window[:8]]
        if len(dims) < 8:
            dims += [0.0]*(8-len(dims))
        vecs.append(dims)
        if len(vecs) >= 8000:   # cap for session
            break
    return vecs


