    def rotate_e8(self, vector: np.ndarray, axis1: int, axis2: int, angle: float) -> np.ndarray:
        """Rotate vector in E8 space around plane defined by axis1, axis2."""
        result = vector.copy()
        cos_a = np.cos(angle)
        sin_a = np.sin(angle)
        x = result[axis1]
        y = result[axis2]
        result[axis1] = cos_a * x - sin_a * y
        result[axis2] = sin_a * x + cos_a * y
        return result

    def scale(self, v: E8Vector, scalar: float) -> E8Vector:
        """Scale a vector."""
        return v * scalar

    def snap(self, vec8d: np.ndarray) -> Tuple[int, float]:
        """
        Snap an 8D vector to the nearest E8 root.

        Args:
            vec8d: 8-dimensional vector

        Returns:
            Tuple of (root_index, distance)
        """
        v = np.array(vec8d, dtype=np.float64)
        dists = np.linalg.norm(self._root_vecs - v, axis=1)
        idx = int(np.argmin(dists))
        return (idx, float(dists[idx]))

    @staticmethod
    def snap_to_lattice(v: np.ndarray) -> np.ndarray:
        """Snap a point to nearest E8 lattice point."""
        int_point = np.round(v)
        if E8Lattice.is_valid_e8_point(int_point):
            return int_point
        half_point = np.round(v - 0.5) + 0.5
        if E8Lattice.is_valid_e8_point(half_point):
            return half_point
        int_point = np.round(v)
        if int(sum(int_point)) % 2 != 0:
            fracs = np.abs(v - int_point)
            idx = np.argmax(fracs)
            int_point[idx] += 1 if v[idx] > int_point[idx] else -1
        return int_point

