    def theta_coefficient(self, n: int) -> int:
        """
        Number of E8 vectors with norm² = 2n.
        Formula: r_E8(2n) = 240 * σ₃(n)
        where σ₃(n) = sum of cubes of divisors of n.
        """

        def sigma3(n: int) -> int:
            return sum((d ** 3 for d in range(1, n + 1) if n % d == 0))
        return 240 * sigma3(n)

    def to_mdhg_address(self, v: E8Vector) -> Dict[str, Any]:
        """
        Convert E8 vector to MDHG address.

        MDHG address format:
        - room, floor, building from first 6 coords
        - city, planet from remaining 2 + hash
        """
        coords = v.coords

        def hash_segment(segment: Tuple[float, ...]) -> str:
            return hashlib.sha256(json.dumps([round(c, 6) for c in segment]).encode()).hexdigest()[:8]
        return {'room': f'room_{hash_segment(coords[0:2])}', 'floor': f'floor_{hash_segment(coords[2:4])}', 'building': f'building_{hash_segment(coords[4:6])}', 'city': f'city_{hash_segment(coords[6:8])}', 'planet': f'planet_e8_{v.fingerprint()[:6]}', 'norm': round(v.norm(), 6)}

