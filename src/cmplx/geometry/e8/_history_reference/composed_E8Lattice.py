    def _babai_closest_vector(self, target: np.ndarray) -> np.ndarray:
        """Babai's algorithm for closest vector in E8 lattice"""
        projection = np.dot(target.reshape(1, -1), self.fundamental_weights.T)
        rounded = np.round(projection)
        closest = np.dot(rounded, self.fundamental_weights).flatten()
        return closest

    def _build_roots(self):
        """Build the complete E8 root system."""
        roots = []
        root_vecs = []

        def add_root(vec, idx):
            vec = np.array(vec, dtype=np.float64)
            roots.append(E8Root(coords=vec, index=idx, norm_squared=2.0, parity=int(sum(vec) >= 0)))
            root_vecs.append(vec)
        idx = 0
        from itertools import combinations
        for i, j in combinations(range(8), 2):
            for s1 in (+1.0, -1.0):
                for s2 in (+1.0, -1.0):
                    vec = [0.0] * 8
                    vec[i] = s1
                    vec[j] = s2
                    add_root(vec, idx)
                    idx += 1
        n_type2 = 128
        for mask in range(1 << 8):
            bits = [mask >> k & 1 for k in range(8)]
            if sum(bits) % 2 == 0:
                vec = [-0.5 if b else 0.5 for b in bits]
                add_root(vec, idx)
                idx += 1
        assert len(roots) == 240, f'Expected 240 roots, got {len(roots)}'
        self._roots = roots
        self._root_vecs = np.array(root_vecs, dtype=np.float64)

    def _compute_cartan_matrix(self) -> np.ndarray:
        """Compute Cartan matrix from simple roots."""
        cartan = np.zeros((8, 8))
        for i in range(8):
            for j in range(8):
                cartan[i, j] = 2 * np.dot(self.simple_roots[i], self.simple_roots[j]) / np.dot(self.simple_roots[j], self.simple_roots[j])
        return cartan

